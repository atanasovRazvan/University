public class Monom {
    private int power;
    private int value;

    public Monom(int power, int value) {
        this.power = power;
        this.value = value;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
